const nota = 70;
if (100 >= nota && nota >= 90){
    console.log("A")
} else if (90 > nota && nota >= 80){
    console.log("B")
} else if (80 > nota && nota >= 70){
    console.log("C")
} else if (70 > nota && nota >= 60) {
    console.log("D")
} else if (60 > nota && nota >= 50) {
    console.log("E")
} else if (50 > nota && nota >= 0) {
    console.log("F")
} else {
    console.log("nota invalida")
}